WITH PARENT as (SELECT *
FROM
    ECOLI_DATA
WHERE
    PARENT_ID is NULL
)

,SECOND as (SELECT 
    ECOLI_DATA.ID,
    ECOLI_DATA.PARENT_ID
FROM
    PARENT
JOIN
    ECOLI_DATA
ON
    PARENT.ID = ECOLI_DATA.PARENT_ID

    )

SELECT
    ECOLI_DATA.ID
FROM
    ECOLI_DATA
JOIN
    SECOND
ON 
    ECOLI_DATA.PARENT_ID = SECOND.ID
ORDER BY ID