

WITH VALUE as (SELECT 
    MAX(FAVORITES) as FAVORITES
FROM
    REST_INFO
GROUP BY
    FOOD_TYPE)


SELECT
    FOOD_TYPE,
    REST_ID,
    REST_NAME,
    VALUE.FAVORITES
FROM
    REST_INFO
JOIN
    VALUE
ON
    REST_INFO.FAVORITES = VALUE.FAVORITES
GROUP BY
    FOOD_TYPE
ORDER BY
    FOOD_TYPE DESC